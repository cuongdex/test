        <h1>Thông tin về trường</h1>
        <div class="thong_tin">
            <p>Trường Công nghệ thông tin và truyền thông (CNTT-TT) vinh dự là một trong bảy khoa/trường đầu tiên ở Việt Nam, đảm trách đào tạo bậc kỹ sư từ năm 1990. Trải qua 33 năm xây dựng và phát triển, thương hiệu Trường CNTT-TT đã trở thành địa chỉ uy tín cung cấp nguồn nhân lực chất lượng cao cho thị trường lao động trong nước và ngoài nước. Trường có nhiệm vụ đào tạo bậc kỹ sư, thạc sĩ và tiến sĩ chuyên về lĩnh vực CNTT-TT và các lĩnh vực liên quan, góp phần tích cực vào sự phát triển của nền công nghiệp công nghệ thông tin Việt Nam; đồng thời thực hiện nghiên cứu khoa học và chuyển giao công nghệ tiên tiến, góp phần đẩy mạnh nghiệp công nghiệp hóa, hiện đại hóa đất nước.</p>
            <p>
            Trường CNTT-TT có 06 khoa và 04 đơn vị trực thuộc. Mỗi khoa đảm trách đào một số ngành và chuyên ngành. Về quy mô đào tạo, Trường hiện đang đảm trách đào tạo 10 ngành và chuyên ngành bậc kỹ sư, 03 ngành bậc thạc sĩ và 01 ngành bậc tiến sĩ, với tổng quy mô hơn 5.000 sinh viên. Về đội ngũ cán bộ, Trường hiện có 123 cán bộ (104 cán bộ giảng dạy và 19 cán bộ hỗ trợ/phục vụ), trong đó có 5 phó giáo sư, 46 tiến sĩ và 58 thạc sĩ. Đội ngũ giảng viên của Trường phần lớn được đào tạo từ các trường đại học tiên tiến ở nước ngoài.
            </p>
